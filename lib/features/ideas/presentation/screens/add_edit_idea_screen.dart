
import 'package:flutter/material.dart';

class AddEditIdeaScreen extends StatefulWidget {
  const AddEditIdeaScreen({super.key});

  @override
  State<AddEditIdeaScreen> createState() => _AddEditIdeaScreenState();
}

class _AddEditIdeaScreenState extends State<AddEditIdeaScreen> {
  final _titleController = TextEditingController();
  final _contentController = TextEditingController();
  @override
  void dispose() {
    _titleController.dispose();
    _contentController.dispose();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(title: const Text('New Idea')),
      body: Padding(
        padding: const EdgeInsets.all(16.0),
        child: Column(
          children: [
            TextField(
              controller: _titleController,
              decoration: const InputDecoration(
                labelText: 'Title',
                border: OutlineInputBorder(),
              ),
            ),
            const SizedBox(height: 16.0),
            Expanded(
              child: TextField(
                controller: _contentController,
                maxLines: null,
                expands: true,
                textAlignVertical: TextAlignVertical.top,
                decoration: const InputDecoration(
                  labelText: 'Content',
                  alignLabelWithHint: true,
                  border: OutlineInputBorder(),
                ),
              ),
            ),
          ],
        ),
      ),
    );
  }
}